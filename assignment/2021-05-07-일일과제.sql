SELECT DEPTNO, ENAME, JOB, SAL, 
    DENSE_RANK() OVER (PARTITION BY DEPTNO ORDER BY SAL DESC) AS GRADE
FROM EMP ORDER BY DEPTNO, SAL DESC;


-- RANK를 사용한 방법
SELECT DEPTNO, ENAME, JOB, SAL,
    RANK() OVER (PARTITION BY DEPTNO ORDER BY SAL DESC) RANK
FROM EMP ORDER BY DEPTNO;

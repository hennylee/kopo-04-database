SELECT E1.DEPTNO, E1.ENAME, E2.DEPTNO, E2.ENAME, E1.SAL, E2.SAL, E1.EMPNO,E1.JOB, E1.MGR, E1.HIREDATE, E1.COMM
FROM EMP E1, EMP E2
WHERE E1.DEPTNO = E2.DEPTNO
